public class switch1 {
    public static void main(String[] args) {
        int a = 10;
        switch (a) {
            case 1:
            case 2:
            case 3:
            case 4:
                System.out.println("hello");
                break;
                case 5:
                System.out.println("hii");
            default:
            System.out.println("bye");
                break;
        }
    }
}
